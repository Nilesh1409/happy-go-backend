// @desc    Verify email
// @route   GET /api/auth/verify-email/:token
// @access  Public
export const verifyEmail = asyncHandler(async (req, res) => {
  // Get hashed token
  const emailVerificationToken = crypto
    .createHash("sha256")
    .update(req.params.token)
    .digest("hex");

  const user = await User.findOne({
    emailVerificationToken,
    emailVerificationExpire: { $gt: Date.now() },
  });

  if (!user) {
    throw new ApiError("Invalid or expired token", 400);
  }

  // Set email as verified
  user.isEmailVerified = true;
  user.emailVerificationToken = undefined;
  user.emailVerificationExpire = undefined;
  await user.save();

  res.status(200).json({
    success: true,
    message: "Email verified successfully",
  });
});

// @desc    Send OTP for mobile verification
// @route   POST /api/auth/send-mobile-otp
// @access  Public
export const sendMobileOTP = asyncHandler(async (req, res) => {
  const { mobile } = req.body;

  // Check if user exists - check both User and Employee models
  const foundUser = await User.findOne({ mobile });
  let foundEmployee = null;
  let entityType = "user";

  if (!foundUser) {
    // If not found in User model, check Employee model
    foundEmployee = await Employee.findOne({ mobile });
    if (foundEmployee) {
      entityType = "employee";
    } else {
      throw new ApiError("User not found", 404);
    }
  }

  // Generate OTP via 2Factor AUTOGEN2
  const smsResult = await sendSMS({ phone: mobile });
  const otp = smsResult.otp; // Use OTP generated by 2Factor

  // Store the 2Factor generated OTP in database
  if (entityType === "user") {
    foundUser.mobileVerificationOTP = otp;
    foundUser.mobileVerificationExpire = Date.now() + 10 * 60 * 1000; // 10 minutes
    await foundUser.save();
  } else {
    // If the entity is an employee, store OTP for them
    foundEmployee.mobileVerificationOTP = otp;
    foundEmployee.mobileVerificationExpire = Date.now() + 10 * 60 * 1000; // 10 minutes
    await foundEmployee.save();
  }

  res.status(200).json({
    success: true,
    message: "OTP sent successfully",
  });
});

